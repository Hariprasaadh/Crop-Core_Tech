import pandas as pd
from sklearn.preprocessing import LabelEncoder
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import r2_score, mean_squared_error

crop_yield_pred_df = pd.read_csv(r"C:\Users\Prannavakhanth\Documents\Crop-Core_Tech\AI\Crop_Yield_Prediction\crop_yield_base.csv")

LE = LabelEncoder()
crop_yield_pred_df["Crop"] = LE.fit_transform(crop_yield_pred_df["Crop"])
crop_yield_pred_df["State"] = LE.fit_transform(crop_yield_pred_df["State"])
crop_yield_pred_df["Season"] = LE.fit_transform(crop_yield_pred_df["Season"])

columns = crop_yield_pred_df.columns
features = crop_yield_pred_df[columns.drop(["Yield", "Crop_Year", "Annual_Rainfall"])]
target = crop_yield_pred_df["Yield"]

X_train, X_test, y_train, y_test = train_test_split(features, target, test_size=0.261, random_state=1)

model = RandomForestRegressor(n_estimators = 100, criterion = "poisson", max_leaf_nodes = 50)
model.fit(X_train, y_train)
predictions = model.predict(X_test)

print(f"R2Score: {r2_score(y_test, predictions): .2f}")

